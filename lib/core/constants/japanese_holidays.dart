  static String _formatDateKey(DateTime date) {
    return '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';
  }

  static bool isHoliday(DateTime date) {
    return _holidays.containsKey(_formatDateKey(date));
  }

  static String? getHolidayName(DateTime date) {
    return _holidays[_formatDateKey(date)];
  }

  static bool isSunday(DateTime date) {
    return date.weekday == DateTime.sunday;
  }

  static bool isSaturday(DateTime date) {
    return date.weekday == DateTime.saturday;
  }

  static bool isWeekend(DateTime date) {
    return isSaturday(date) || isSunday(date);
  }

  static bool isRedDay(DateTime date) {
    return isSunday(date) || isHoliday(date);
  }
